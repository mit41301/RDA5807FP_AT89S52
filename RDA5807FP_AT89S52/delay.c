void delayNOP()
{
   _nop_();_nop_();_nop_();_nop_();_nop_();
}

/**********************************************************

 ms延时子程序

**********************************************************/
void delayms( unsigned int ms) 
{
   unsigned char k;
   while(ms--)
   {
     for(k = 0; k < 120; k++);
   }
}

